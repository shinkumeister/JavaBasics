package com.syntax.class01;

public class VariableValues {

	public static void main(String[] args) {
		

		byte hundred = 100;
		short thousand = 1000;
		int hundredk = 100000;
		long onemil = 1000000L;
		float small = 1.99F;
		double big = 99.99;
		char letter= 'a';
		char sign = '$';
		boolean yes = true;
		boolean no = false;
		

		System.out.println(hundred);
		System.out.println(thousand);
		System.out.println(hundredk);
		System.out.println(onemil);
		System.out.println(small);
		System.out.println(big);
		System.out.println(letter);
		System.out.println(sign);
		System.out.println(yes);
		System.out.println(no);
		System.out.println();
		
		hundred = 101;
		thousand = 1001;
		hundredk = 100001;
		onemil = 1000001L;
		small = 1.98F;
		big = 99.98;
		letter= 'b';
		sign = '%';
		yes = false;
		no = true;
		
		System.out.println(hundred);
		System.out.println(thousand);
		System.out.println(hundredk);
		System.out.println(onemil);
		System.out.println(small);
		System.out.println(big);
		System.out.println(letter);
		System.out.println(sign);
		System.out.println(yes);
		System.out.println(no);
	}

}
